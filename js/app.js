// ===== 志愿合规性校验 =====
function validateVolunteers(list) {
    var warnings = [], errors = [], infos = [];
    if (!state.selectedCategory) return { warnings: warnings, errors: errors, infos: infos };
    // 跨类校验
    for (var i = 0; i < list.length; i++) {
        var v = list[i];
        if (!isSameCategory(v.category, state.selectedCategory)) {
            errors.push('\u7b2c' + (i + 1) + '\u4f4d ' + v.uniName + '-' + v.majorName + '\uff1a\u4e13\u4e1a\u7c7b\u522b\u300c' + (v.category || '\u672a\u77e5') + '\u300d\u4e0e\u62a5\u8003\u7c7b\u522b\u300c' + state.selectedCategory + '\u300d\u4e0d\u5339\u914d\uff01\u4e0d\u53ef\u8de8\u7c7b\u586b\u62a5\uff01');
        }
    }
    // 梯度校验
    var reachCount = 0, stableCount = 0, safeCount = 0;
    for (var i = 0; i < list.length; i++) {
        var cls = list[i].classification || classifyVolunteer(list[i].skillScore2025, list[i].culturalScore2025);
        if (cls === '\u51b2') reachCount++;
        else if (cls === '\u7a33') stableCount++;
        else if (cls === '\u4fdd') safeCount++;
    }
    if (list.length > 0) {
        if (reachCount === 0) warnings.push('\u65b9\u6848\u4e2d\u6ca1\u6709\u300c\u51b2\u523a\u300d\u5fd7\u613f\uff0c\u53ef\u80fd\u9519\u8fc7\u66f4\u597d\u7684\u9662\u6821');
        if (safeCount === 0) warnings.push('\u65b9\u6848\u4e2d\u6ca1\u6709\u300c\u4fdd\u5e95\u300d\u5fd7\u613f\uff0c\u5b58\u5728\u6ed1\u6863\u98ce\u9669\uff01\u5efa\u8bae\u81f3\u5c11\u6dfb\u52a01-2\u4e2a');
        if (reachCount > list.length * 0.5) warnings.push('\u300c\u51b2\u523a\u300d\u5360\u6bd4\u8fc7\u9ad8\uff08' + reachCount + '/' + list.length + '\uff09\uff0c\u5efa\u8bae\u589e\u52a0\u7a33\u59a5\u548c\u4fdd\u5e95\u5fd7\u613f');
        infos.push('\u51b2\u7a33\u4fdd\u5206\u5e03\uff1a\u51b2' + reachCount + '(' + Math.round(reachCount/list.length*100) + '%) / \u7a33' + stableCount + '(' + Math.round(stableCount/list.length*100) + '%) / \u4fdd' + safeCount + '(' + Math.round(safeCount/list.length*100) + '%)');
    }
    // 重复校验
    var seen = {};
    for (var i = 0; i < list.length; i++) {
        var key = list[i].uniId + '-' + list[i].majorCode;
        if (seen[key]) warnings.push('\u7b2c' + (i + 1) + '\u4f4d\u548c\u7b2c' + seen[key] + '\u4f4d\u91cd\u590d\uff1a' + list[i].uniName + '-' + list[i].majorName);
        seen[key] = i + 1;
    }
    // 退档风险
    if (!state.obeyAdjust) {
        for (var i = 0; i < list.length; i++) {
            var v = list[i];
            var cls = v.classification || classifyVolunteer(v.skillScore2025, v.culturalScore2025);
            if (cls === '\u51b2' || cls === '\u7a33') warnings.push('\u7b2c' + (i + 1) + '\u4f4d ' + v.uniName + '-' + v.majorName + '\uff1a\u672a\u52fe\u9009\u670d\u4ece\u8c03\u5242\uff0c\u53ef\u80fd\u88ab\u9000\u6863\uff01');
        }
    }
    // 分数低于投档线
    for (var i = 0; i < list.length; i++) {
        var v = list[i];
        if (state.skillScore < v.skillScore2025) warnings.push('\u7b2c' + (i + 1) + '\u4f4d ' + v.uniName + '-' + v.majorName + '\uff1a\u6280\u80fd\u5206' + state.skillScore + ' < \u6295\u6863\u7ebf' + v.skillScore2025);
        if (state.culturalScore < v.culturalScore2025) warnings.push('\u7b2c' + (i + 1) + '\u4f4d ' + v.uniName + '-' + v.majorName + '\uff1a\u6587\u5316\u5206' + state.culturalScore + ' < \u6295\u6863\u7ebf' + v.culturalScore2025);
    }
    return { warnings: warnings, errors: errors, infos: infos };
}

// ===== 整体方案评分 =====
function getOverallScore(list) {
    if (list.length === 0) return { score: 0, label: '\u672a\u586b\u5199', color: '#94a3b8' };
    var reachCount = 0, stableCount = 0, safeCount = 0;
    for (var i = 0; i < list.length; i++) {
        var cls = list[i].classification || classifyVolunteer(list[i].skillScore2025, list[i].culturalScore2025);
        if (cls === '\u51b2') reachCount++; else if (cls === '\u7a33') stableCount++; else if (cls === '\u4fdd') safeCount++;
    }
    var total = list.length, score = 0;
    if (safeCount > 0) score += 10; if (stableCount > 0) score += 10;
    if (reachCount > 0 && reachCount <= total * 0.4) score += 10;
    if (stableCount >= total * 0.3) score += 10;
    if (total >= 10) score += 15; else if (total >= 5) score += 10; else score += 5;
    if (total >= 15) score += 15;
    if (safeCount >= 2) score += 15; else if (safeCount >= 1) score += 8;
    if (reachCount <= total * 0.5) score += 15; else if (reachCount <= total * 0.7) score += 8;
    var label, color;
    if (score >= 80) { label = '\u4f18\u79c0'; color = '#16a34a'; }
    else if (score >= 60) { label = '\u826f\u597d'; color = '#d97706'; }
    else if (score >= 40) { label = '\u4e00\u822c'; color = '#f59e0b'; }
    else { label = '\u9700\u8c03\u6574'; color = '#dc2626'; }
    return { score: score, label: label, color: color };
}

