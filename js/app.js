// ===== Level tag injection =====
function injectLevelTags() {
    var cards = document.querySelectorAll('.uni-card');
    cards.forEach(function(card) {
        if (card.querySelector('.tag-level')) return;
        var info = card.querySelector('.uni-info');
        if (!info) return;
        var nameEl = info.querySelector('.uni-name');
        if (!nameEl) return;
        var uniName = nameEl.textContent;
        var uni = APP_DATA.universities.find(function(u) {
            return u.name === uniName && u.majors.some(function(m) { return isSameCategory(m.category, state.selectedCategory); });
        });
        if (uni && uni.level) {
            var tagClass = uni.level === '\u672c\u79d1' ? 'tag-ug' : 'tag-vc';
            var tag = document.createElement('span');
            tag.className = 'uni-tag tag-level ' + tagClass;
            tag.textContent = uni.level;
            info.appendChild(tag);
        }
    });
}

