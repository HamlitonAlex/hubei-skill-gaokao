function toggleUni(uniId) {
    // 点击大学头部时，切换该大学同类别所有专业的展开状态
    var parts = uniId.split('-');
    var baseId = parts[0]; // 从复合键中提取大学ID
    var catFilter = parts[1] || ''; // 提取类别过滤
    var allMajors = [];
    for (var i = 0; i < APP_DATA.universities.length; i++) {
        if (APP_DATA.universities[i].id === baseId) {
            for (var j = 0; j < APP_DATA.universities[i].majors.length; j++) {
                var m = APP_DATA.universities[i].majors[j];
                if (!catFilter || isSameCategory(m.category, catFilter)) {
                    allMajors.push(m.code);
                }
            }
            break;
        }
    }
    // 判断当前是否已展开（任意一个专业展开即为已展开）
    var anyExpanded = allMajors.some(function(mc) { return state.expandedMajors.has(mc); });
    if (anyExpanded) {
        allMajors.forEach(function(mc) { state.expandedMajors.delete(mc); });
    } else {
        allMajors.forEach(function(mc) { state.expandedMajors.add(mc); });
    }
    rerenderResults();
}

