// ===== Matching Logic =====
function classifyVolunteer(skillScore, culturalScore) {
    if (!state.skillScore || !state.culturalScore) return null;
    var skillDiff = state.skillScore - skillScore;
    var culturalDiff = state.culturalScore - culturalScore;
    var minDiff = Math.min(skillDiff, culturalDiff);
    if (minDiff < -30) return null; // 分数差距过大，不推荐
    if (minDiff >= -30 && minDiff < 0) return '\u51b2';
    if (minDiff >= 0 && minDiff <= 10) return '\u7a33';
    if (minDiff > 10) return '\u4fdd';
    return null;
}

// ===== 录取概率评估 =====
// 根据分数差距计算录取概率评级
function getProbabilityRating(skillScore, culturalScore) {
    if (!state.skillScore || !state.culturalScore) return { label: '\u672a\u77e5', level: 0, color: '#94a3b8', bg: '#f1f5f9' };
    var skillDiff = state.skillScore - skillScore;
    var culturalDiff = state.culturalScore - culturalScore;
    var minDiff = Math.min(skillDiff, culturalDiff);
    if (minDiff < -30) return { label: '\u51b2\u523a', level: 1, color: '#dc2626', bg: '#fef2f2' };
    if (minDiff >= -30 && minDiff < -10) return { label: '\u8f83\u7a33', level: 2, color: '#f59e0b', bg: '#fffbeb' };
    if (minDiff >= -10 && minDiff < 0) return { label: '\u7a33\u59a5', level: 3, color: '#d97706', bg: '#fffbeb' };
    if (minDiff >= 0 && minDiff <= 10) return { label: '\u7a33\u59a5', level: 3, color: '#d97706', bg: '#fffbeb' };
    if (minDiff > 10 && minDiff <= 30) return { label: '\u7a33\u59a5', level: 4, color: '#16a34a', bg: '#f0fdf4' };
    if (minDiff > 30) return { label: '\u4fdd\u5e95', level: 5, color: '#059669', bg: '#ecfdf5' };
    return { label: '\u672a\u77e5', level: 0, color: '#94a3b8', bg: '#f1f5f9' };
}

function getMatchedResults() {
    if (!state.skillScore || !state.culturalScore) return { '\u51b2': [], '\u7a33': [], '\u4fdd': [] };
    var results = { '\u51b2': [], '\u7a33': [], '\u4fdd': [] };
    var unis = APP_DATA.universities;

    for (var i = 0; i < unis.length; i++) {
        var uni = unis[i];
        for (var j = 0; j < uni.majors.length; j++) {
            var major = uni.majors[j];
            if (state.selectedCategory && !isSameCategory(major.category, state.selectedCategory)) continue;
            var cls = classifyVolunteer(major.skillScore2025, major.culturalScore2025);
            if (!cls) continue; // 分数差距过大，跳过
            var match = {
                uniId: uni.id, uniName: uni.name, uniType: uni.type, uniLocation: uni.location,
                majorName: major.name, majorCode: major.code, enrollment: major.enrollment,
                tuition: major.tuition, campus: major.campus,
                skillScore2025: major.skillScore2025, culturalScore2025: major.culturalScore2025,
                skillScore2024: major.skillScore2024, culturalScore2024: major.culturalScore2024,
                skillScore2023: major.skillScore2023, culturalScore2023: major.culturalScore2023,
                classification: cls, category: major.category, level: major.level || uni.level || '专科'
            };
            results[cls].push(match);
        }
    }
    return results;
}

function applyFilters(results) {
    var filtered = { '\u51b2': results['\u51b2'].slice(), '\u7a33': results['\u7a33'].slice(), '\u4fdd': results['\u4fdd'].slice() };
    var f = state.filters;
    var tabs = ['\u51b2', '\u7a33', '\u4fdd'];
    for (var t = 0; t < tabs.length; t++) {
        var tab = tabs[t];
        filtered[tab] = filtered[tab].filter(function(m) {
            if (f.type !== 'all' && m.uniType !== f.type) return false;
            if (f.location === '\u6b66\u6c49' && m.uniLocation !== '\u6b66\u6c49') return false;
            if (f.location === '\u5730\u5e02' && m.uniLocation === '\u6b66\u6c49') return false;
            if (f.level && f.level !== 'all') {
                var mLevel = m.level || '专科';
                if (f.level === '本科' && mLevel !== '本科') return false;
                if (f.level === '专科' && mLevel !== '专科') return false;
            }
            if (f.skillMin && m.skillScore2025 < parseInt(f.skillMin)) return false;
            if (f.skillMax && m.skillScore2025 > parseInt(f.skillMax)) return false;
            if (f.culturalMin && m.culturalScore2025 < parseInt(f.culturalMin)) return false;
            if (f.culturalMax && m.culturalScore2025 > parseInt(f.culturalMax)) return false;
            return true;
        });
    }
    return filtered;
}

