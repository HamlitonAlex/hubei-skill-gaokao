// ===== Rank Calculation =====
function getRank(score, category) {
    if (!APP_DATA.rankTable || !category || !APP_DATA.rankTable[category]) return null;
    var table = APP_DATA.rankTable[category];
    var keys = Object.keys(table).map(Number).sort(function(a, b) { return b - a; }); // descending
    if (keys.length === 0) return null;

    // Exact match
    if (table[String(score)] !== undefined) return table[String(score)];

    // Score outside range
    if (score >= keys[0]) return table[String(keys[0])];
    if (score <= keys[keys.length - 1]) return table[String(keys[keys.length - 1])];

    // Interpolate between nearest scores
    var higher = null, lower = null;
    for (var i = 0; i < keys.length - 1; i++) {
        if (score < keys[i] && score > keys[i + 1]) {
            higher = keys[i];
            lower = keys[i + 1];
            break;
        }
    }
    if (higher === null || lower === null) return null;
    var rankHigh = table[String(higher)];
    var rankLow = table[String(lower)];
    var ratio = (score - lower) / (higher - lower);
    return Math.round(rankLow + (rankHigh - rankLow) * ratio);
}

function getTotalParticipants(category) {
    if (!APP_DATA.rankTable || !APP_DATA.rankTable[category]) return null;
    var table = APP_DATA.rankTable[category];
    var keys = Object.keys(table).map(Number).sort(function(a, b) { return a - b; });
    if (keys.length === 0) return null;
    return table[String(keys[0])];
}

function getScoreFromRank(rank, category) {
    if (!APP_DATA.rankTable || !category || !APP_DATA.rankTable[category]) return null;
    var table = APP_DATA.rankTable[category];
    var keys = Object.keys(table).map(Number).sort(function(a, b) { return b - a; }); // descending
    if (keys.length === 0) return null;

    // Exact match
    for (var i = 0; i < keys.length; i++) {
        if (table[String(keys[i])] === rank) return keys[i];
    }

    // Find bracket
    for (var i = 0; i < keys.length - 1; i++) {
        var rankA = table[String(keys[i])];
        var rankB = table[String(keys[i + 1])];
        if ((rank >= rankA && rank <= rankB) || (rank <= rankA && rank >= rankB)) {
            var ratio = (rank - rankB) / (rankA - rankB);
            return Math.round(keys[i + 1] + ratio * (keys[i] - keys[i + 1]));
        }
    }
    // Outside range
    if (rank <= table[String(keys[0])]) return keys[0];
    return keys[keys.length - 1];
}

