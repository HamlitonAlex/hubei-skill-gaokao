// ===== Smart Recommend =====
function generateSmartRecommend() {
    if (!state.skillScore || !state.culturalScore) return;
    var raw = getMatchedResults();
    var reachList = raw['\u51b2'].slice();
    var stableList = raw['\u7a33'].slice();
    var safeList = raw['\u4fdd'].slice();

    // Sort each by closeness to user score
    var sortByDist = function(a, b) {
        var distA = Math.abs(state.skillScore - a.skillScore2025) + Math.abs(state.culturalScore - a.culturalScore2025);
        var distB = Math.abs(state.skillScore - b.skillScore2025) + Math.abs(state.culturalScore - b.culturalScore2025);
        return distA - distB;
    };
    reachList.sort(sortByDist);
    stableList.sort(sortByDist);
    safeList.sort(sortByDist);

    // 冲30%稳40%保30% = 20个志愿方案
    var picks = [];
    var maxReach = Math.min(6, reachList.length);
    var maxStable = Math.min(8, stableList.length);
    var maxSafe = Math.min(6, safeList.length);
    // 如果总数不足20，按比例调整
    var totalAvailable = reachList.length + stableList.length + safeList.length;
    if (totalAvailable < 20) {
        var ratio_r = reachList.length / totalAvailable;
        var ratio_s = stableList.length / totalAvailable;
        var ratio_f = safeList.length / totalAvailable;
        maxReach = Math.min(reachList.length, Math.max(1, Math.round(20 * ratio_r)));
        maxStable = Math.min(stableList.length, Math.max(1, Math.round(20 * ratio_s)));
        maxSafe = Math.min(safeList.length, Math.max(1, Math.round(20 * ratio_f)));
    }
    for (var i = 0; i < maxReach; i++) picks.push(reachList[i]);
    for (var i = 0; i < maxStable; i++) picks.push(stableList[i]);
    for (var i = 0; i < maxSafe; i++) picks.push(safeList[i]);

    // Render
    var container = document.getElementById('smartRecommendResult');
    if (!container) return;

    var html = '<div class="recommend-result">';
    html += '<div class="recommend-stats">';
    html += '<div class="recommend-stat reach"><div class="stat-num">' + maxReach + '</div><div class="stat-label">\u51b2 - \u8bd5\u4e00\u8bd5(30%)</div></div>';
    html += '<div class="recommend-stat stable"><div class="stat-num">' + maxStable + '</div><div class="stat-label">\u7a33 - \u628a\u63e1\u5927(40%)</div></div>';
    html += '<div class="recommend-stat safe"><div class="stat-num">' + maxSafe + '</div><div class="stat-label">\u4fdd - \u786e\u4fdd\u5f55\u53d6(30%)</div></div>';
    html += '</div>';

    html += '<div class="recommend-list" style="background:#f8fafc;border-radius:14px;overflow:hidden">';
    for (var j = 0; j < picks.length; j++) {
        var p = picks[j];
        var cls = p.classification;
        var clsColor = cls === '\u51b2' ? '#dc2626' : cls === '\u7a33' ? '#d97706' : '#16a34a';
        var clsBg = cls === '\u51b2' ? '#fef2f2' : cls === '\u7a33' ? '#fffbeb' : '#f0fdf4';
        var rankClass = j === 0 ? 'r1' : j === 1 ? 'r2' : j === 2 ? 'r3' : 'rn';
        var inVol = state.volunteers.some(function(v) { return v.uniId === p.uniId && v.majorCode === p.majorCode; });

        html += '<div class="recommend-item">' +
            '<div class="recommend-rank ' + rankClass + '">' + (j + 1) + '</div>' +
            '<div class="sv-tag" style="background:' + clsBg + ';color:' + clsColor + '">' + cls + '</div>' +
            '<div style="flex:1;min-width:0">' +
                '<div style="font-weight:700;font-size:15px">' + p.uniName + '</div>' +
                '<div style="font-size:13px;color:#64748b">' + p.majorName + ' | \u6280\u80fd' + p.skillScore2025 + ' \u6587\u5316' + p.culturalScore2025 + ' | \u62db\u751f' + p.enrollment + '\u4eba</div>' +
            '</div>' +
            '<button class="btn-add ' + (inVol ? 'added' : '') + '" style="flex-shrink:0" onclick="addRecommendToVolunteer(\'' + p.uniId + '\',\'' + p.majorCode + '\')" ' + (inVol ? 'disabled' : '') + '>' + (inVol ? '\u5df2\u52a0\u5165' : '+ \u52a0\u5165') + '</button>' +
        '</div>';
    }
    html += '</div>';

    html += '<div style="text-align:center;margin-top:16px">' +
        '<button class="recommend-btn" style="background:#0d76cc;box-shadow:0 4px 16px rgba(13,118,204,.35)" onclick="addAllRecommendToVolunteer()">' +
        '\u4e00\u952e\u52a0\u5165\u5168\u90e8\u63a8\u8350\u5fd7\u613f</button></div>';

    html += '</div>';
    container.innerHTML = html;

    // Store picks for batch add
    state._recommendPicks = picks;
}

function addRecommendToVolunteer(uniId, majorCode) {
    var unis = APP_DATA.universities.filter(function(u) {
        if (u.id !== uniId) return false;
        if (state.selectedCategory) {
            var hasMatchingMajor = u.majors.some(function(m) { return isSameCategory(m.category, state.selectedCategory); });
            if (!hasMatchingMajor) return false;
        }
        return true;
    });
    if (unis.length === 0) return;
    var uni = unis[0];
    var major = null;
    for (var j = 0; j < uni.majors.length; j++) {
        if (uni.majors[j].code === majorCode) { major = uni.majors[j]; break; }
    }
    if (!major) return;
    var match = {
        uniId: uni.id, uniName: uni.name, uniType: uni.type, uniLocation: uni.location,
        majorName: major.name, majorCode: major.code, enrollment: major.enrollment,
        tuition: major.tuition, campus: major.campus,
        skillScore2025: major.skillScore2025, culturalScore2025: major.culturalScore2025,
        skillScore2024: major.skillScore2024, culturalScore2024: major.culturalScore2024,
        skillScore2023: major.skillScore2023, culturalScore2023: major.culturalScore2023,
        classification: classifyVolunteer(major.skillScore2025, major.culturalScore2025),
        category: major.category,
        level: uni.level || '\u4e13\u79d1'
    };
    toggleVolunteer(match);
    generateSmartRecommend();
}

function addAllRecommendToVolunteer() {
    if (!state._recommendPicks) return;
    var picks = state._recommendPicks;
    var added = 0;
    for (var i = 0; i < picks.length; i++) {
        var p = picks[i];
        // 检查是否已存在（两个批次都要检查）
        var existsBenke = state.volunteers.some(function(v) { return v.uniId === p.uniId && v.majorCode === p.majorCode; });
        var existsZhuanke = state.volunteersVocational.some(function(v) { return v.uniId === p.uniId && v.majorCode === p.majorCode; });
        if (existsBenke || existsZhuanke) continue;
        // 根据院校层次自动分配到对应批次
        var targetList = (p.level === '\u672c\u79d1') ? state.volunteers : state.volunteersVocational;
        if (targetList.length >= 20) continue;
        var count = targetList.filter(function(v) { return v.uniId === p.uniId; }).length;
        if (count >= 6) continue;
        if (!p.classification) p.classification = classifyVolunteer(p.skillScore2025, p.culturalScore2025);
        targetList.push(p);
        added++;
    }
    renderTopNav();
    rerenderResults();
    renderSelectedVolunteers();
    generateSmartRecommend();
    if (added > 0) {
        alert('\u5df2\u52a0\u5165' + added + '\u4e2a\u63a8\u8350\u5fd7\u613f\uff01');
    } else {
        alert('\u6240\u6709\u63a8\u8350\u5fd7\u613f\u5df2\u5728\u5217\u8868\u4e2d');
    }
}

