function startTour() {
    var overlay = document.createElement('div');
    overlay.id = 'tourOverlay';
    overlay.style.cssText = 'position:fixed;top:0;left:0;width:100%;height:100%;background:rgba(0,0,0,.55);z-index:9998;transition:opacity .3s';
    document.body.appendChild(overlay);

    var stepIdx = 0;
    function showStep() {
        var step = tourSteps[stepIdx];
        var target = document.querySelector(step.el);
        var tooltip = document.getElementById('tourTooltip');
        if (!tooltip) {
            tooltip = document.createElement('div');
            tooltip.id = 'tourTooltip';
            tooltip.style.cssText = 'position:fixed;z-index:9999;background:#fff;border-radius:14px;padding:20px 24px;max-width:320px;box-shadow:0 20px 60px rgba(0,0,0,.3);font-size:14px;line-height:1.7;transition:all .3s';
            document.body.appendChild(tooltip);
        }
        tooltip.innerHTML =
            '<div style="font-size:16px;font-weight:700;color:#0f172a;margin-bottom:6px">' + step.title + '</div>' +
            '<div style="color:#475569;margin-bottom:16px">' + step.text + '</div>' +
            '<div style="display:flex;justify-content:space-between;align-items:center">' +
            '<span style="font-size:12px;color:#94a3b8">' + (stepIdx + 1) + ' / ' + tourSteps.length + '</span>' +
            '<div style="display:flex;gap:8px">' +
            (stepIdx > 0 ? '<button id="tourPrev" style="padding:6px 14px;border:1px solid #e2e8f0;background:#fff;border-radius:8px;font-size:13px;cursor:pointer;color:#475569">\u4e0a\u4e00\u6b65</button>' : '') +
            '<button id="tourNext" style="padding:6px 18px;border:none;background:#0d76cc;color:#fff;border-radius:8px;font-size:13px;cursor:pointer;font-weight:600">' + (stepIdx < tourSteps.length - 1 ? '\u4e0b\u4e00\u6b65' : '\u5b8c\u6210') + '</button>' +
            '</div></div>' +
            '<button id="tourSkip" style="position:absolute;top:8px;right:8px;background:none;border:none;color:#94a3b8;font-size:18px;cursor:pointer;padding:4px 8px">&times;</button>';

        if (target) {
            var rect = target.getBoundingClientRect();
            var top = rect.bottom + 12;
            var left = rect.left + rect.width / 2 - 160;
            if (left < 10) left = 10;
            if (left + 320 > window.innerWidth) left = window.innerWidth - 330;
            if (top + 180 > window.innerHeight) top = rect.top - 180;
            tooltip.style.top = top + 'px';
            tooltip.style.left = left + 'px';
            target.style.position = 'relative';
            target.style.zIndex = '9999';
        } else {
            tooltip.style.top = '50%';
            tooltip.style.left = '50%';
            tooltip.style.transform = 'translate(-50%,-50%)';
        }

        document.getElementById('tourNext').onclick = function() {
            if (target) { target.style.zIndex = ''; }
            if (stepIdx < tourSteps.length - 1) { stepIdx++; showStep(); }
            else { endTour(); }
        };
        var prevBtn = document.getElementById('tourPrev');
        if (prevBtn) {
            prevBtn.onclick = function() {
                if (target) { target.style.zIndex = ''; }
                stepIdx--; showStep();
            };
        }
        document.getElementById('tourSkip').onclick = endTour;
    }

    function endTour() {
        var overlay = document.getElementById('tourOverlay');
        var tooltip = document.getElementById('tourTooltip');
        if (overlay) overlay.remove();
        if (tooltip) tooltip.remove();
        document.querySelectorAll('[style*="z-index: 9999"]').forEach(function(el) { el.style.zIndex = ''; });
        localStorage.setItem('hubei\u5fd7\u613f\u5f15\u5bfc\u5df2\u5b8c\u6210', '1');
    }

    showStep();
}

